"""
El uso del while permite ejecutar una sección 
de código repetidas veces, de ahí su nombre.

El código se ejecutará mientras una condición determinada se cumpla. Cuando se deje de cumplir, 
se saldrá del bucle y se continuará la ejecución normal.
Llamaremos iteración a una ejecución completa del bloque de código.
"""

i=0 #El valor que tiene la variable cuando empieza
while i<=5:  #hasta el valor que tiene que llegar
  print(f"iteracion: {i}")
  i+=1 #para que cada vez que entre al buble +1 a la variable